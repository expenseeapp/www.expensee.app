function Tab({ title, type, url }) {
  const { theme } = useTheme()
  const [mounted, setMounted] = useState(false)
  const dark = theme === 'dark'

  useEffect(() => {
    setMounted(true)
  }, [])

  if (!mounted) {
    return null
  }

  const icons = dark ? darkIcons : lightIcons

  const handleLinkClick = () => {
    mixpanel.track('App Store Link Clicked')
  }

  return (
    <Link
      onClick={handleLinkClick}
      target="_blank"
      href={url}
      rel="noreferrer"
      className={styles.Container}
    >
      <div className={styles.TabContainer}>
        <div className={styles.TabIconContainer}>{icons[type]}</div>
        <div className={styles.TabTitleContainer}>{title}</div>
      </div>
    </Link>
  )
}

Tab.propTypes = {
  title: PropTypes.string,
  type: PropTypes.string,
  url: PropTypes.string,
}

export default Tab
